    private void BotonCrearEstudianteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonCrearEstudianteActionPerformed
        try{
            Estudiante estudiante = new Estudiante();
            estudiante.setId(TextIdEstudiante.getText());
            estudiante.setNombre(TextNombeEstudiante.getText());
            estudiante.setEdad(TextEdadEstudiante.getText());
            estudiante.setSexo(ComboSexoEstudiante.getSelectedItem().toString());
            estudiante.setGrado(gradoLogica.obtenerGradoPorId(Integer.parseInt(ComboGradoEstudiante.getSelectedItem().toString())));
            estudiante.setUniforme(TextUniformeEstudiante.getText());

            if (TextIdEstudiante.getText().isEmpty() || TextNombeEstudiante.getText().isEmpty() || TextEdadEstudiante.getText().isEmpty() || TextUniformeEstudiante.getText().isEmpty()) {
                JOptionPane.showMessageDialog(PanelCompleto, "Todos los campos son obligatorios", "Registrar Estudiante", JOptionPane.ERROR_MESSAGE);
            } 
            
            if (estudianteLogica.validarEstudianteId(estudiante.getId())) {
                JOptionPane.showMessageDialog(PanelCompleto, "El Id debe ser Ãºnico e irrepetible", "Registrar Estudiante", JOptionPane.ERROR_MESSAGE);
            } else {
                estudianteLogica.crearEstudiante(estudiante);
                BotonLimpiarEstudianteActionPerformed(evt);
                JOptionPane.showMessageDialog(PanelCompleto, "Se ha registrado el estudiante: " + estudiante.getNombre(), "Registrar Estudiante", JOptionPane.INFORMATION_MESSAGE);
            }
        }catch(Exception e){
        }
    }//GEN-LAST:event_BotonCrearEstudianteActionPerformed

