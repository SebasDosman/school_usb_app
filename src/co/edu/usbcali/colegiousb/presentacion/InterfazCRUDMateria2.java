    private void BotonCrearMateriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonCrearMateriaActionPerformed
        try {
            Materia materia = new Materia();
            materia.setId(TextIdMateriaMateria.getText());
            materia.setNombre(TextNombreMateria.getText());
            materia.setDocente(empleadoLogica.obtenerEmpleadoPorId(TextEmpleadoMateria.getText()));
            materia.setContenido(TextContenidoMateria.getText());
            materia.setGrado(gradoLogica.obtenerGradoPorId(Integer.parseInt(ComboGrado.getSelectedItem().toString())));
            materia.setHorario(TextHorarioMateria.getText());
        
            if (empleadoLogica.obtenerEmpleadoPorId(TextEmpleadoMateria.getText()) == null) {
                JOptionPane.showMessageDialog(PanelCompleto, "El id del docente no existe", "Registrar Materia", JOptionPane.ERROR_MESSAGE);
            }
            
            if (TextIdMateriaMateria.getText().isEmpty() || TextNombreMateria.getText().isEmpty() || TextEmpleadoMateria.getText().isEmpty() || TextContenidoMateria.getText().isEmpty() || TextHorarioMateria.getText().isEmpty()) {
                JOptionPane.showMessageDialog(PanelCompleto, "Todos los campos son obligatorios", "Registrar Materia", JOptionPane.ERROR_MESSAGE);
            }
            
            if (materiaLogica.validarMateriaId(materia.getId())) {
                JOptionPane.showMessageDialog(PanelCompleto, "El Id debe ser Ãºnico e irrepetible", "Registrar Materia", JOptionPane.ERROR_MESSAGE);
            } else if (!materiaLogica.validarDocenteMateria(TextEmpleadoMateria.getText())) {
                JOptionPane.showMessageDialog(PanelCompleto, "No se puede asignar un empleado que no sea docente a una materia", "Registrar Materia", JOptionPane.ERROR_MESSAGE);
            } else {
                materiaLogica.crearMateria(materia);
                BotonLimpiarMateriaActionPerformed(evt);
                JOptionPane.showMessageDialog(PanelCompleto, "Se ha registrado la materia: " + materia.getNombre(), "Registrar Materia", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_BotonCrearMateriaActionPerformed

